package model.game;

public class GamePlayers {

	private String white;
	private String black;
	
	
	
	public GamePlayers() {
		super();
	}
	public GamePlayers(String white, String black) {
		super();
		this.white = white;
		this.black = black;
	}
	public String getWhite() {
		return white;
	}
	public void setWhite(String white) {
		this.white = white;
	}
	public String getBlack() {
		return black;
	}
	public void setBlack(String black) {
		this.black = black;
	}
	
	
	
}
